def get_recommendations(risk_level: str, goal: str, category: str = "全部") -> tuple[list, str | None]:
    allowed_risks = RISK_FILTER.get(risk_level, ["低"])
    picks = [
        p for p in BEGINNER_PICKS
        if p["risk"] in allowed_risks
        and goal in p["goals"]
        and (category == "全部" or p["category"] == category)
    ]
    msg = EMPTY_MESSAGES.get((risk_level, goal)) if not picks else None
    return picks, msg
